# MAIN
# ============================================================

def main() -> None:
    print("=" * 86)
    print("RECURSIVE WHEEL PRIME/TWIN GENERATOR")
    print("=" * 86)
    print(f"Maximum number:          {MAX_NUMBER:,}")
    print(f"Recursive wheel primes: {WHEEL_PRIME_COUNT}")

    wheel_start = perf_counter()

    wheel = RecursiveWheel.from_recursive_primes(
        WHEEL_PRIME_COUNT
    )

    wheel_elapsed = perf_counter() - wheel_start

    print(
        "Generated wheel primes:  "
        + ", ".join(
            str(prime)
            for prime in wheel.primes
        )
    )
    print(
        f"Wheel modulus:           "
        f"{wheel.modulus:,}"
    )
    print(
        f"Admissible wheel states: "
        f"{len(wheel.residues):,}"
    )
    print(
        f"Wheel density:           "
        f"{len(wheel.residues) / wheel.modulus:.6%}"
    )
    print(
        f"Wheel build time:        "
        f"{wheel_elapsed:.6f} seconds"
    )
    print("External prime tests:    none")
    print("Full Boolean sieve:      none")
    print()

    generator = RecursiveWheelPrimeCycles(
        maximum=MAX_NUMBER,
        wheel=wheel,
    )

    logic = TwinLogicStats()
    samples = SampleWindow(
        SAMPLE_SIZE
    )

    csv_file = None
    csv_writer = None

    if WRITE_TWINS_CSV:
        csv_file = CSV_PATH.open(
            "w",
            newline="",
            encoding="utf-8",
        )
        csv_writer = csv.writer(csv_file)
        csv_writer.writerow(
            [
                "lower_prime",
                "centre",
                "upper_prime",
                "centre_index",
                "three_bit_address",
                "metallic_address",
            ]
        )

    start = perf_counter()

    previous_prime = None
    highest_twin: TwinSample | None = None

    try:
        for prime in generator.primes():
            if (
                previous_prime is not None
                and prime - previous_prime == 2
            ):
                sample = logic.record(
                    previous_prime,
                    prime,
                )

                samples.add(sample)
                highest_twin = sample

                if csv_writer is not None:
                    if sample.centre_index is None:
                        bit_address = ""
                        metal_address = ""
                    else:
                        bit_address = three_bit_address(
                            sample.centre_index
                        )
                        metal_address = metallic_address(
                            sample.centre_index
                        )

                    csv_writer.writerow(
                        [
                            sample.lower,
                            sample.centre,
                            sample.upper,
                            (
                                ""
                                if sample.centre_index is None
                                else sample.centre_index
                            ),
                            bit_address,
                            metal_address,
                        ]
                    )

            previous_prime = prime
    finally:
        if csv_file is not None:
            csv_file.close()

    elapsed = perf_counter() - start

    odd_baseline = (MAX_NUMBER - 1) // 2
    integer_baseline = max(0, MAX_NUMBER - 1)

    visited = generator.stats.wheel_candidates_visited

    print("=" * 86)
    print("SUMMARY")
    print("=" * 86)
    print(
        f"Prime openings:             "
        f"{generator.stats.prime_openings:,}"
    )
    print(
        f"Twin-prime pairs:           "
        f"{logic.total_twins:,}"
    )
    print(
        f"Ordinary 6n twins:          "
        f"{logic.ordinary_twins:,}"
    )
    print(
        f"Exceptional twins:          "
        f"{logic.exceptional_twins:,}"
    )
    print(
        f"Wheel addresses visited:    "
        f"{visited:,}"
    )
    print(
        f"Odd-address baseline:       "
        f"{odd_baseline:,}"
    )
    print(
        f"Reduction from odd scan:    "
        f"{1 - visited / odd_baseline:.6%}"
    )
    print(
        f"Reduction from integer scan:"
        f" {1 - visited / integer_baseline:.6%}"
    )
    print(
        f"Prime cycles opened:        "
        f"{generator.stats.cycle_openings:,}"
    )
    print(
        f"Cycle completions:          "
        f"{generator.stats.cycle_completions:,}"
    )
    print(
        f"Collision advances:         "
        f"{generator.stats.collision_advances:,}"
    )
    print(
        f"Maximum active events:      "
        f"{generator.stats.maximum_active_events:,}"
    )
    print(
        f"Elapsed time:               "
        f"{elapsed:.6f} seconds"
    )

    if highest_twin is not None:
        print("\nHighest generated twin:")
        print_sample(highest_twin)
        print(
            f"    rank among generated twins: "
            f"{logic.total_twins:,}"
        )
        print(
            f"    twin pairs before it:        "
            f"{logic.total_twins - 1:,}"
        )

    print_radix8_shell_table(logic)

    print_counter(
        "Least-significant exact three-bit state",
        logic.low_digit_counts,
    )

    print_counter(
        "Least-significant metallic layer",
        logic.layer_counts,
    )

    print_counter(
        "Trailing completed 111 lift blocks",
        logic.trailing_lift_counts,
    )

    if PRINT_TWO_BLOCK_LOGIC_TABLE:
        print_two_block_table(
            logic.low_two_block_counts
        )

    if PRINT_SAMPLES:
        print("\n" + "=" * 86)
        print("FIRST SAMPLE")
        print("=" * 86)

        for sample in samples.first:
            print_sample(sample)

        print("\n" + "=" * 86)
        print("LAST SAMPLE")
        print("=" * 86)

        for sample in samples.last:
            print_sample(sample)

    if WRITE_TWINS_CSV:
        print(
            f"\nCSV written to: "
            f"{CSV_PATH.resolve()}"
        )

    print("\nInterpretation:")
    print(
        "The recursive wheel removes addresses belonging to the "
        "internally generated cycles of its wheel primes."
    )
    print(
        "The remaining recursive cycles generate every surviving prime."
    )
    print(
        "The radix-8 shell table counts twin addresses at each "
        "self-similar lift scale without retaining all twins."
    )
    print(
        "The next compression problem is to derive those shell and "
        "suffix counts directly from the recursive state transition."
    )


if __name__ == "__main__":
    main()
